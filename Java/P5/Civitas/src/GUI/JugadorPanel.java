    private void rellenaPropiedades(ArrayList<tituloPropiedad> lista){
        propiedades.removeAll();
        
        for(tituloPropiedad t:lista){
            PropiedadPanel vistaPropiedad=new PropiedadPanel();
            vistaPropiedad.setPropiedad(t);
            vistaPropiedad.setVisible(true);
            propiedades.add(vistaPropiedad);
            vistaPropiedad.setVisible(true);
        }
        
        propiedades.repaint();
        propiedades.revalidate();
        
        repaint();
        revalidate();
    }
    
    void setJugador(Jugador jugador){
        this.jugador=jugador;
        Nombre.setText("Nombre: ");
        Saldo.setText("Saldo: ");
        Encarcelado.setText("Encarcelado: ");
        Espc.setText("Especulador: ");
        Casilla.setText("NÃºmero de casilla actual: ");
        Salvc.setText("Salvoconducto: ");
        nombre.setText(jugador.getNombre());
        saldo.setText(Float.toString(jugador.getSaldo()));
        encarcelado.setText(Boolean.toString(jugador.estaEncarcelado()));
        especulador.setText(Boolean.toString(jugador.esEspeculador()));
        casilla.setText(Integer.toString(jugador.getNumCasillaActual()));
        salvoconducto.setText(Boolean.toString(jugador.tieneSalvoconducto()));
        rellenaPropiedades(jugador.getPropiedades());
        repaint();
    }
